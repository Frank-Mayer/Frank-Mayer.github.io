const emoteMap = new Map<string, string>()
const emoteRegex = /:([^:]+):/g

export function mapEmotes(text: string, allowHtml: boolean = false): string {
    if (!text) {
        return text
    }
    return text.replace(emoteRegex, (match, p1) => {
        const emote = emoteMap.get(p1)
        if (emote && (allowHtml || emote.length < 4)) {
            return emote
        }
        return match
    })
}

